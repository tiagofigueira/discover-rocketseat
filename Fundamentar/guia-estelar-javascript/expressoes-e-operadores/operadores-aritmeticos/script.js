// Operadores Aritméticos

// multiplicação - O operador de multiplicação é o * (asterisco);
let multiplicacao = 3.2 * 5.5;
console.log("Multiplicando números --> 3.2 * 5.5 <-- é: " + multiplicacao)

// divisão - O operador de divisão é a / (barra);
let divisao = 12 / 2;
console.log("Divisão de número --> 12 / 2 <-- é: " + divisao)

// soma - O operador de soma é o + (positivo);
let soma = 5 + 5
console.log("Soma de números --> 5 + 5 <-- é: " + soma)

// subtração - O operador de subtração é o - (negativo).
let subtracao = 5 + 5
console.log("Subtração de números --> 5 - 5 <-- é: " + subtracao)



// resto da divisão 
/* resto da divisão: sinal % */
let remainder = 11 % 9
console.log("Resto da divisão 11 % 9 é: " + remainder)

// incremento  - incremento: sinal ++ *
let increment = 0
console.log("Incremento ao valor ++0 é: " + increment)

// decremento - decremento: sinal --
let decrement = 0
decrement--
console.log("Decremento do valor --0 é: " + decrement)

// exponencial - exponencial: sinal **
let exponencial = 2 ** 3
console.log("Exemplo de exponencial 2 ** 3: " + exponencial)