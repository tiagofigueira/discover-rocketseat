let one = 1
let two = 2

// >    Maior que
console.log("Maior que >")
console.log(one > two)

// >=    Maior igual a
console.log("Maior igual a >=")
console.log(one >= 1)
console.log(two >= 1)

// <    Menor que
console.log("Menor que <")
console.log(one < two)

// >=    Menor igual a
console.log("Menor igual a <=")
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
